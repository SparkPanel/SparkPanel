    /**
     * Поиск по документации
     */
    public function searchDocumentation($query) {
        // В реальной реализации здесь будет полноценный поиск по документации
        $results = [];
        
        $sections = $this->getDocumentationSections();
        foreach ($sections as $section) {
            if (stripos($section['title'], $query) !== false || stripos($section['description'], $query) !== false) {
                $results[] = $section;
            }
        }
        
        return $results;
    }
    
